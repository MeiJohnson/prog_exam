import random
def main():
    """
    Создание программы по заполнению массивов случайными значениями. 
    Сортировка значений в списке методом вставки, 
    плавной сортировки, с помощью встроенных функций языка.
    """
    N = int(input("Введите размер массива "))
    lst = [random.randint(1,100) for i in range(N)]
    print(lst)
    lst.sort()
    print(lst)
main()